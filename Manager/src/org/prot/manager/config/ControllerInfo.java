package org.prot.manager.config;

public class ControllerInfo
{
	private String address; 
	
	private int port;

	
	public String getAddress()
	{
		return address;
	}

	public void setAddress(String address)
	{
		this.address = address;
	}

	public int getPort()
	{
		return port;
	}

	public void setPort(int port)
	{
		this.port = port;
	} 
}
